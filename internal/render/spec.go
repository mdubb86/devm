package render

import (
	"fmt"
	"sort"
	"strings"

	"github.com/mtwaage/devm/internal/schema"
)

// SpecYAML generates the sbx kit spec.yaml from the validated config.
// repoRoot is the project's absolute path; used in volume mount paths.
func SpecYAML(cfg schema.Config, repoRoot string) string {
	image := "docker/sandbox-templates:shell"
	if cfg.BaseImage.Docker {
		image = "docker/sandbox-templates:shell-docker"
	}

	var sb strings.Builder
	sb.WriteString("# Generated by devm reconcile. DO NOT EDIT.\n")
	sb.WriteString(fmt.Sprintf("schemaVersion: \"1\"\nkind: agent\nname: %s\n", cfg.Project.ID))
	sb.WriteString(fmt.Sprintf("displayName: %s dev\n", cfg.Project.ID))
	sb.WriteString(fmt.Sprintf("description: devm-managed dev sandbox for %s\n\n", cfg.Project.ID))

	sb.WriteString("agent:\n")
	sb.WriteString(fmt.Sprintf("  image: %s\n", image))
	sb.WriteString("  aiFilename: CLAUDE.md\n")
	sb.WriteString("  persistence: persistent\n")
	sb.WriteString("  entrypoint:\n")
	// Entrypoint is shell-wrapped sleep infinity with stdin redirected
	// from /dev/null. Two load-bearing pieces:
	//   1. sh -c wrapping: sbx daemon propagates session-end cleanup
	//      to the in-VM process for shell-wrapped entrypoints but not
	//      bare commands. exec replaces sh with sleep so no extra
	//      process lingers.
	//   2. </dev/null redirect: detaches sleep's fd 0 from the pty
	//      that sbx run allocates for the anchor. Without it the
	//      sleep keeps an open fd to /dev/pts/0 even after killRun(),
	//      making it appear as a phantom session in devm stop's
	//      session listing (sessions.go walks /proc for pts/* fd0
	//      holders). The redirect is purely cosmetic for lifecycle
	//      (sbx daemon's session count is independent of this) but
	//      makes the "Active sessions" listing truthful.
	sb.WriteString("    run: [\"sh\", \"-c\", \"exec sleep infinity </dev/null\"]\n\n")

	if len(cfg.Network.AllowedDomains) > 0 {
		sb.WriteString("network:\n  allowedDomains:\n")
		for _, d := range cfg.Network.AllowedDomains {
			sb.WriteString(fmt.Sprintf("    - %s\n", d))
		}
		sb.WriteString("\n")
	}

	// Collect ALL masks across services and sort by service name for deterministic output.
	type mEntry struct {
		Path string
		Size string
	}
	var allMasks []mEntry
	names := make([]string, 0, len(cfg.Services))
	for name := range cfg.Services {
		names = append(names, name)
	}
	sort.Strings(names)
	for _, name := range names {
		for _, m := range cfg.Services[name].Masks {
			allMasks = append(allMasks, mEntry{Path: m.Path, Size: m.Size})
		}
	}
	if len(allMasks) > 0 {
		sb.WriteString("volumes:\n")
		for _, m := range allMasks {
			sb.WriteString(fmt.Sprintf("  %q: size=%s\n", repoRoot+"/"+m.Path, m.Size))
		}
		sb.WriteString("\n")
	}

	sb.WriteString("environment:\n  variables:\n    IS_SANDBOX: \"1\"\n\n")

	sb.WriteString("commands:\n")

	// install: emit only if user defined any install steps.
	if len(cfg.Install) > 0 {
		sb.WriteString("  install:\n")
		for _, cmd := range cfg.Install {
			sb.WriteString(fmt.Sprintf("    - command: %s\n", yamlSingleQuoted(cmd)))
		}
		sb.WriteString("\n")
	}

	// startup: built-in init-volumes (always first), then per-service
	// startup commands in service-sorted order. Declaration order is
	// preserved within each service.
	sb.WriteString("  startup:\n")
	sb.WriteString("    - command: ['bash', '-c', 'exec bash \"$WORKSPACE_DIR/.devm/scripts/init-volumes.sh\"']\n")
	sb.WriteString("      user: \"1000\"\n")
	sb.WriteString("      description: Claim ext4 volume mounts for agent user\n")
	sb.WriteString("    - command: ['bash', '-c', 'exec bash \"$WORKSPACE_DIR/.devm/scripts/install-templates.sh\"']\n")
	sb.WriteString("      user: \"0\"\n")
	sb.WriteString("      description: Install rendered service templates\n")

	// Aggregate per-service startup steps in service-name-sorted order.
	// `names` is the sorted service-names slice defined earlier in
	// SpecYAML for the masks loop.
	for _, name := range names {
		for idx, step := range cfg.Services[name].Startup {
			writeStartupStep(&sb, step, name, idx)
		}
	}
	sb.WriteString("\n")

	return sb.String()
}

// yamlSingleQuoted wraps s in single quotes, escaping any inner single
// quotes by doubling them. This is YAML's escape rule for single-quoted
// scalars.
func yamlSingleQuoted(s string) string {
	return "'" + strings.ReplaceAll(s, "'", "''") + "'"
}

// writeStartupStep emits one startup step entry. When the user sets
// `background: true` in devm.yaml, we render a FOREGROUND sbx kit step
// (no `background: true` in the kit YAML) whose command is wrapped with
// shell-level `nohup ... &`. This matches the pattern used by the real
// docker/sbx-kits-contrib kits (e.g. code-server).
//
// Why not the kit's own `background: true` field? Empirically the kit
// flag is for short-lived setup steps and the process is killed ~5s
// after launch. For long-running services (the actual use case for
// `services.<name>.startup`) the only working pattern is foreground +
// shell `nohup ... &`. See docs/sbx-quirks.md quirk #5 and the
// regression guard at e2e/test_sbx_13_background_daemon_lifetime.py.
func writeStartupStep(sb *strings.Builder, step schema.StartupCommand, service string, idx int) {
	argv := step.Command
	if step.Background {
		argv = backgroundWrap(step.Command, service, idx)
	}
	quoted := make([]string, len(argv))
	for i, a := range argv {
		quoted[i] = yamlSingleQuoted(a)
	}
	sb.WriteString(fmt.Sprintf("    - command: [%s]\n", strings.Join(quoted, ", ")))
	if step.Background {
		sb.WriteString("      user: \"1000\"\n")
		sb.WriteString(fmt.Sprintf("      description: %s\n",
			yamlSingleQuoted(fmt.Sprintf("%s startup daemon", service))))
		// NOTE: intentionally NOT emitting `background: true` here —
		// the shell-level `&` in backgroundWrap handles backgrounding,
		// and the kit's `background: true` flag has a ~5s kill timer.
	}
}

// backgroundWrap wraps user argv as ['sh', '-c', 'nohup <args> > <log> 2>&1 &'].
// The shell-level `&` returns immediately (so the startup step finishes
// without blocking), and `nohup` detaches the child so it survives the
// parent shell exit.
func backgroundWrap(userArgv []string, service string, idx int) []string {
	quoted := make([]string, len(userArgv))
	for i, a := range userArgv {
		quoted[i] = "'" + strings.ReplaceAll(a, "'", `'\''`) + "'"
	}
	logPath := fmt.Sprintf("/tmp/devm-startup-%s-%d.log", service, idx)
	cmd := fmt.Sprintf("nohup %s > '%s' 2>&1 &",
		strings.Join(quoted, " "), logPath)
	return []string{"sh", "-c", cmd}
}
